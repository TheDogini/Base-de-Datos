-- ESCUELA
CREATE DATABASE IF NOT EXISTS escuela;
USE escuela;

CREATE TABLE IF NOT EXISTS salon(
    id_salon INT PRIMARY KEY AUTO_INCREMENT,
    letra CHAR(1) NOT NULL,
    cantidad_alumnos INT NOT NULL,
    especialidad VARCHAR(100) NOT NULL
);

CREATE TABLE IF NOT EXISTS estudiante(
    id_estudiante INT PRIMARY KEY AUTO_INCREMENT,
    nombre VARCHAR(100) NOT NULL,
    edad INT NOT NULL,
    telefono VARCHAR(20),
    correo VARCHAR(100) NOT NULL,
    promedio_pasado DECIMAL(4,2),
    id_salon INT NOT NULL,
    FOREIGN KEY (id_salon) REFERENCES salon(id_salon) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS maestro(
    id_maestro INT PRIMARY KEY AUTO_INCREMENT,
    nombre VARCHAR(100) NOT NULL,
    edad INT NOT NULL,
    telefono VARCHAR(20) NOT NULL,
    correo VARCHAR(100) NOT NULL
);

CREATE TABLE IF NOT EXISTS materia(
    id_materia INT PRIMARY KEY AUTO_INCREMENT,
    nombre VARCHAR(100) NOT NULL,
    horas INT NOT NULL,
    tasa_de_aprobacion DECIMAL(4,2) NOT NULL
);

CREATE TABLE IF NOT EXISTS maestro_materia (
    id_maestro INT NOT NULL,
    id_materia INT NOT NULL,
    PRIMARY KEY (id_maestro, id_materia),
    FOREIGN KEY (id_maestro) REFERENCES maestro(id_maestro),
    FOREIGN KEY (id_materia) REFERENCES materia(id_materia)
);

CREATE TABLE IF NOT EXISTS maestro_salon (
    id_maestro INT NOT NULL,
    id_salon INT NOT NULL,
